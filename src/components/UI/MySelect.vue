<template>
  <select class="select" @change="changeOption">
    <option disabled value="">Выберите из списка</option>
    <option
      v-for="option in $store.state.post.sortOptions"
      :key="option.value"
      :value="option.value"
    >
      {{ option.name }}
    </option>
  </select>
</template>
<script>
import { mapGetters } from "vuex";

export default {
  name: "my-select",
  data() {
    return {
      sortedPosts: this.getSortedPosts,
    }
  },
  computed: {
    ...mapGetters(["getSortedPosts"]),
  },
  methods: {
    changeOption(e) {
      this.$store.dispatch('getSortedPosts', e.target.value)
    }
  }
};
</script>
